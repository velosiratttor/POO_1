#3. Implementa una clase 'Calculadora' con métodos para sumar, restar, multiplicar y dividir dos números.

class calculadora:
    
    def sumar (a , b):
        return a + b
    
    def restar(a , b):
        return a - b
    
    def multiplicar (a , b):
        return a * b
    
    def dividir(a , b):
        return a / b
    
numero = calculadora
print(numero.sumar(10, 20))
